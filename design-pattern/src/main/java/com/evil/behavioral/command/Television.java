package com.evil.behavioral.command;

public class Television {
    public void turnOn() {
        System.out.println("Turning on the television.");
    }

    public void turnOff() {
        System.out.println("Turning off the television.");
    }

    public void increaseVolume() {
        System.out.println("Increasing the volume.");
    }

    public void decreaseVolume() {
        System.out.println("Decreasing the volume.");
    }
}
